package com.example.junmung.StudyHelper.Drawer;

public class DrawerItem {
    private int img;
    private String name;

    public DrawerItem(int img, String name) {
        this.img = img;
        this.name = name;
    }
    public int getImg() {
        return img;
    }

    public String getName() {
        return name;
    }

}
